#Crie um programa que recebe um númro de telefone com 11 dígitos.
#1. O número é considerado inválido se tiver 3 ou mais dígitos iguais.
#2. O programa deve verificar se o número tem 11 dígitos e se todos os caracteres são numéricos.
#3. Se o número for válido, o programa deve formata-lo para o padrão (XX) XXXXX-XXXX.
#4. O programa deve imprimir o número formatado ou a mensagem de erro correspondente.

